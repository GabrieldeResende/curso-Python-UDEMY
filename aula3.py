"""
tipo de dados
str - string - textos "assim" ou 'assim'
int - inteiro - 12345 ou -1-2-3-4-5
float - real - 1.2 1.5 ou -1.5 -1.2
bool - booleano - true/false, verdadeiro/falso 10==10
"""
print("gabriel", type("gabriel"))
print(23, type(23))
print(1.70, type(1.70))
print(23 > 18, type(23 > 18))
print(10 == 10)
print("\n \n")
'''
tipo de operadores aritmeticos
+ adição
- subtração
* multiplicação
** exponenciação 
/ divisao
// divisao inteira
% resto de divisao
'''
print(10 * 10)  # multiplicação
print(10 + 10)  # adição
print(10 - 5)  # subtração
